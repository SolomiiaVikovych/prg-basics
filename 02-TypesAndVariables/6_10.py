
movie = "The Lord of the Rings: The Return of the King"
num_characters = len(movie)
print('Number of characters: ', num_characters)
title_upper = movie.upper()
print('Title in capital letters: ', title_upper)
title_lower = movie.lower()
print('Title in small letters: ', title_lower)
e_count = movie.count('e')
print('Count of the vowel "e": ', e_count)
lord_position = movie.find('Lord')
print('Position of the word "Lord": ', lord_position)
dragon_position = movie.find('dragon')
print('Position of the word "dragon": ', dragon_position)
